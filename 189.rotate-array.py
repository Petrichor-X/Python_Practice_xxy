# @lc code=start
class Solution(object):
    def rotate(self, nums, k):
        """
        :type nums: List[int]
        :type k: int
        :rtype: None Do not return anything, modify nums in-place instead.
        """
        k %= len(nums)  # 确保 k 不超过数组长度
        nums[:] = nums[-k:] + nums[:-k]

